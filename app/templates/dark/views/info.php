				Somos um servidor de MU Online, um jogo de (MMORPG) 3D, 
				ambientado numa atmosfera medieval. O jogo foi desenvolvido pela 
				companhia coreana Webzen. Disponibilizamos o jogo em portugu&ecirc;s 
				e com certas regalias que no servidor oficial da WebZen voc&ecirc; n&atilde;o encontra.
				<br /><br />
				<p class="title">COMO &Eacute; O JOGO MU ONLINE?</p>
				Como na maior parte dos MMORPGs existentes, voc&ecirc; selecionar&aacute; 
				um personagem e enfretar&aacute; monstros (mobs) para ganhar 
				experi&ecirc;ncia. O continente de MU &eacute; habitado por 
				uma grande variedade de monstros, dos mais simples como goblins 
				e golems aos mais temidos como Selupan ou Nightmare. Cada tipo 
				de monstro &eacute; unico, possuindo diferente "spawn points", 
				e "drops" variados de itens.
				<br /><br />
				<p class="title">COMO JOGAR?</p>
				<span class="white">PASSO 1:</span> Clique em 'Download's', baixe nosso tipo de download 
				mais compativel com voc&ecirc;/computador, logo ap&oacute;s instale 
				(Instala&ccedil;&atilde;o normal do tipo de qualquer software 
				existe).<br />
				<span class="white">PASSO 2:</span> Logo ap&oacute;s crie sua conta aqui no site em 
				'Registrar'.<br />
				<span class="white">PASSO 3:</span> V&aacute; at&eacute; a &Aacute;rea de trabalho procure 
				por 'MU Mithos Hard' abra o launcher, espere ele verificar por 
				atualiza&ccedil;&otilde;es e logo ap&oacute;s quando o bot&atilde;o 
				'Iniciar Jogo' estiver liberado voc&ecirc; pode iniciar sua 
				jornada pelo MU Online.
